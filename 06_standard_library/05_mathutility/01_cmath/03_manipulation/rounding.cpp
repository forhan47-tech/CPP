#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double x = 3.75;
    cout << "fabs(" << x << ") = " << fabs(x) << endl;
    cout << "ceil(" << x << ") = " << ceil(x) << endl;
    cout << "floor(" << x << ") = " << floor(x) << endl;
    cout << "round(" << x << ") = " << round(x) << endl;
    cout << "trunc(" << x << ") = " << trunc(x) << endl;
    return 0;
}
