#include <iostream>
using namespace std;

int main() {
    int x = 5, y = 10;
    cout << (x == y) << endl; // Equal?
    cout << (x != y) << endl; // Not equal?
    cout << (x > y) << endl;  // Greater?
    cout << (x < y) << endl;  // Less?
    cout << (x >= y) << endl; // Greater or equal?
    cout << (x <= y) << endl; // Less or equal?
    return 0;
}
