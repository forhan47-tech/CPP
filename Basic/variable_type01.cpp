#include <iostream>
using namespace std;

int main() {
    int age = 25;             // Integer
    char grade = 'A';         // Character
    float pi = 3.14f;         // Float
    double e = 2.718281828;   // Double
    bool isPassed = true;     // Boolean

    cout << "Age: " << age << endl;
    cout << "Grade: " << grade << endl;
    cout << "Pi: " << pi << endl;
    cout << "Euler's number: " << e << endl;
    cout << "Passed? " << isPassed << endl;
    return 0;
}
