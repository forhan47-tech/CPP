#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double x = 9.0;

    cout << "Square root of " << x << " is " << sqrt(x) << endl;  // Output: 3.0
    cout << "2 raised to the power 3 is " << pow(2, 3) << endl;   // Output: 8.0
    cout << "Absolute value of -7.5 is " << abs(-7.5) << endl;    // Output: 7.5

    return 0;
}

