#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double x = 9.0;

    cout << "Square root of " << x << " is " << sqrt(x) << endl; 
    cout << "2 raised to the power 3 is " << pow(2, 3) << endl;       
    cout << "Absolute value of -7.5 is " << abs(-7.5) << endl; 
      
    return 0;
}

